class Scope { 
    public static void main(String[] args) {

        
        for(int i = 1; i < 5; i++) {
            System.out.println(i);
        }
        

        for(int j=5;j<7;j++)
        {
            System.out.println(j);
        }
    
          
        for (int k = 7; k < 11; k++) {
            System.out.println(k);
        }

    }
}
